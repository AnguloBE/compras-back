import { Injectable, OnModuleInit, OnModuleDestroy } from '@nestjs/common';
import { PrismaClient } from '@prisma/client';
import { PrismaPg } from '@prisma/adapter-pg';
import { Pool } from 'pg';

@Injectable()
export class PrismaService implements OnModuleInit, OnModuleDestroy {
  private prisma: PrismaClient;
  private pool: Pool;

  constructor() {
    this.pool = new Pool({ connectionString: process.env.DATABASE_URL });
    const adapter = new PrismaPg(this.pool);
    this.prisma = new PrismaClient({ adapter });
  }

  async onModuleInit() {
    await this.prisma.$connect();
  }

  async onModuleDestroy() {
    await this.prisma.$disconnect();
    await this.pool.end();
  }

  // Proxy para acceder a los m√©todos de Prisma
  get usuario() {
    return this.prisma.usuario;
  }

  get categoria() {
    return this.prisma.categoria;
  }

  get ubicacion() {
    return this.prisma.ubicacion;
  }

  get producto() {
    return this.prisma.producto;
  }

  get pedido() {
    return this.prisma.pedido;
  }

  get detallePedido() {
    return this.prisma.detallePedido;
  }

  get horarioAtencion() {
    return this.prisma.horarioAtencion;
  }
}
